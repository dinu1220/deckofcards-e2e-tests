package models;

public class Images
{
    private String svg;

    private String png;

    public void setSvg(String svg){
        this.svg = svg;
    }
    public String getSvg(){
        return this.svg;
    }
    public void setPng(String png){
        this.png = png;
    }
    public String getPng(){
        return this.png;
    }
}